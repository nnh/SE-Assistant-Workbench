import {
  outputRowMap,
  removeCommasAndSpaces_,
  roundToNearest100_,
} from './common';
import { coefficient_10_2015 } from './forTest_coefficient_10_2015';
class convertFrom2015To2025 {
  constructor() {}
  convertFrom2015To2025(coefficient: string): string[][] {
    const coefficientValue =
      coefficient === 'coefficient10'
        ? 1
        : coefficient === 'coefficient15'
          ? 1.5
          : 0;
    const unitPriceIndex = outputRowMap.get('unitPrice');
    const minorIndex = outputRowMap.get('minor');
    const minorsWithoutCoefficient = [
      '名古屋医療センターCRB申請費用(初年度)',
      '名古屋医療センターCRB申請費用(2年目以降)',
      '外部監査費用',
      '施設監査費用',
    ];
    if (unitPriceIndex === undefined || minorIndex === undefined) {
      throw new Error('unitPrice or minor index not found in outputRowMap');
    }
    const dayIndex: number = unitPriceIndex + 1;
    const personCountIndex: number = unitPriceIndex + 2;
    const converted2025: string[][] = coefficient_10_2015.map((row, index) => {
      if (index === 0) {
        // Header row remains unchanged
        return row;
      }
      if (row[unitPriceIndex] === '') {
        return row;
      }
      const unitPrice: string = removeCommasAndSpaces_(row[unitPriceIndex]);
      const unitPrice2025: number = Math.round(Number(unitPrice) * 1.07);
      const basePrice2025: number =
        row[minorIndex] === '問い合わせ対応'
          ? roundToNearest100_(
              (unitPrice2025 *
                Number(row[dayIndex]) *
                Number(row[personCountIndex])) /
                8
            )
          : row[minorIndex] === '名古屋医療センターCRB申請費用(初年度)'
            ? 800000 * 1.07
            : row[minorIndex] === '名古屋医療センターCRB申請費用(2年目以降)'
              ? 200000 * 1.07
              : roundToNearest100_(
                  unitPrice2025 *
                    Number(row[dayIndex]) *
                    Number(row[personCountIndex])
                );
      const price2025: number = roundToNearest100_(
        minorsWithoutCoefficient.includes(row[minorIndex])
          ? basePrice2025
          : basePrice2025 * coefficientValue
      );
      const resultRow: string[] = [...row];
      resultRow[outputRowMap.get('basePrice')!] = basePrice2025.toString();
      resultRow[unitPriceIndex] = unitPrice2025.toString();
      resultRow[outputRowMap.get('price')!] = price2025.toString();
      return resultRow;
    });
    return converted2025;
  }
}
export function convertPriceFrom2015To2025_(): Map<string, string[][]> {
  const converter = new convertFrom2015To2025();
  const coefficient10_2025: string[][] =
    converter.convertFrom2015To2025('coefficient10');
  const coefficient15_2025: string[][] =
    converter.convertFrom2015To2025('coefficient15');
  const coefficient2025Map: Map<string, string[][]> = new Map([
    ['coefficient10', coefficient10_2025],
    ['coefficient15', coefficient15_2025],
  ]);
  return coefficient2025Map;
}
